public class Unboxing {

    public static void main(String[] args) {
        Integer a=10;
        Float b=10.2F;
        Character c='h';

        int x=a;
        float y=b;
        char z=c;

        System.out.println(x);
        System.out.println(y);
        System.out.println(z);

    }
    
}
