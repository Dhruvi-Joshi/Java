public class Callstrack {
    public static void main(String[] args) {
        int x=0;
        System.out.println(x);
        first();
    }
    static void first(){
        int x=10;
        System.out.println(x);
        second();
    }
    static void second(){
        int x=20;
        System.out.println(x);
        third();
    }
    static void third(){
        int x=30;
        System.out.println(x);
    }
}
